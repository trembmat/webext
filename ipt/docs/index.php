<h1>I+ Internet PHP Toolkit</h1>


<h2>Documentation</h2>


<p>

I+ Internet PHP Toolkit est un ensemble de classes PHP et de scripts de différents languages qui optimisera
votre temps de conception d'applications et/ou pages web. Les différentes classes comprises dans 
I+IPT vous permettent de créer directement votre propre base de données, d'accéder à vos
données, de créer des formulaires de saisie, de convertir vos données en différents formats 
et d'automatiser différents processus en un rien de temps.
</p>




